    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblTransaksi.getSelectedRow();
if (selectedRow != -1) {
    int confirmDialogResult = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data transaksi ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    if (confirmDialogResult == JOptionPane.YES_OPTION) {
        String kodePemesanan = (String) tblTransaksi.getValueAt(selectedRow, 0);
        transaksiController.deleteTransaksi(kodePemesanan);
        loadDataTransaksi(); // Memuat ulang data setelah menghapus transaksi
    }
} else {
    JOptionPane.showMessageDialog(this, "Pilih salah satu Data Transaksi untuk dihapus.");
}
    }//GEN-LAST:event_btnDeleteActionPerformed

