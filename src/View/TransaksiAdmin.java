    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTransaksi.getSelectedRow();
        if (selectedRow != -1) {
            String kodePemesanan = (String) tblTransaksi.getValueAt(selectedRow, 0);
            TransaksiModel transaksi = transaksiController.getTransaksiByKodePemesanan(kodePemesanan);
            if (transaksi != null) {
                String newPemesan = JOptionPane.showInputDialog(this, "Masukkan Pemesan:", transaksi.getPemesan());
                String newNomorKamarStr = JOptionPane.showInputDialog(this, "Masukkan Nomor Kamar:", String.valueOf(transaksi.getNomorKamar()));
                int newNomorKamar = Integer.parseInt(newNomorKamarStr);
                String newCheckIn = JOptionPane.showInputDialog(this, "Masukkan Check-In:", transaksi.getCheckIn());
                String newCheckOut = JOptionPane.showInputDialog(this, "Masukkan Check-Out:", transaksi.getCheckOut());
                String newPembayaran = JOptionPane.showInputDialog(this, "Masukkan Pembayaran:", transaksi.getPembayaran());
                String newDibayarkanStr = JOptionPane.showInputDialog(this, "Masukkan Dibayarkan:", String.valueOf(transaksi.getDibayarkan()));
                int newDibayarkan = Integer.parseInt(newDibayarkanStr);

                transaksi.setPemesan(newPemesan);
                transaksi.setNomorKamar(String.valueOf(newNomorKamar)); // Convert to String
                transaksi.setCheckIn(newCheckIn);
                transaksi.setCheckOut(newCheckOut);
                transaksi.setPembayaran(newPembayaran);
                transaksi.setDibayarkan(newDibayarkan);

                transaksiController.updateTransaksi(transaksi);
                loadDataTransaksi();
            }
        } else {
             JOptionPane.showMessageDialog(this, "Pilih salah satu Data Transaksi untuk diedit.");
}
        

    }//GEN-LAST:event_btnEditActionPerformed

