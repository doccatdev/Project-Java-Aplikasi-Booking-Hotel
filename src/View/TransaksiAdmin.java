    private void loadDataTransaksi() {
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("Kode Pemesanan");
        tableModel.addColumn("Pemesan");
        tableModel.addColumn("Nomor Kamar");
        tableModel.addColumn("Check-In");
        tableModel.addColumn("Check-Out");
        tableModel.addColumn("Pembayaran");
        tableModel.addColumn("Dibayarkan");

        // Ambil data transaksi dari controller
        List<TransaksiModel> transaksiList = transaksiController.getAllTransaksi();
        for (TransaksiModel transaksi : transaksiList) {
            Object[] rowData = {
                transaksi.getKodePemesanan(),
                transaksi.getPemesan(),
                transaksi.getNomorKamar(),
                transaksi.getCheckIn(),
                transaksi.getCheckOut(),
                transaksi.getPembayaran(),
                transaksi.getDibayarkan()
            };
            tableModel.addRow(rowData);
        }

        // Terapkan model tabel pada jTable1
        tblTransaksi.setModel(tableModel);
    }

