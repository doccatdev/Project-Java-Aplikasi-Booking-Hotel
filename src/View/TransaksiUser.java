    private void updateTransaksi() {
    String kodePemesanan = txtKode.getText();
    String pemesan = txtPesan.getText();
    String nomorKamar = txtNKamar.getText();
    String checkIn = txtIn.getText();
    String checkOut = txtOut.getText();
    String pembayaran = txtBayar.getText();
    double dibayarkan = Double.parseDouble(txtUang.getText());

    TransaksiModel transaksi = new TransaksiModel();
    transaksi.setKodePemesanan(kodePemesanan);
    transaksi.setPemesan(pemesan);
    transaksi.setNomorKamar(nomorKamar);
    transaksi.setCheckIn(checkIn);
    transaksi.setCheckOut(checkOut);
    transaksi.setPembayaran(pembayaran);
    transaksi.setDibayarkan(dibayarkan);

    transaksiController.updateTransaksi(transaksi);

    txtUang.setText(String.valueOf(transaksi.getDibayarkan()));
    
    }
    
