    private void btnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanActionPerformed
        // TODO add your handling code here:
          String kodePemesanan = txtKode.getText();
          String pemesan = txtPesan.getText();
          String nomorKamar = txtNKamar.getText();

    // Mengecek apakah kode pemesanan sudah ada di database
    if (transaksiController.isKodePemesananExists(kodePemesanan)) {
        System.out.println("Kode pemesanan sudah ada. Transaksi dibatalkan.");
        return; // Menghentikan proses penyimpanan
    }
    // Get the selected check-in and check-out dates from date picker fields
    String checkIn = txtIn.getText(); 
    String checkOut = txtOut.getText(); 

    String pembayaran = txtBayar.getText(); // Ganti dengan metode pembayaran yang sesuai
    double dibayarkan = 0;

    // Menghitung lama tinggal dalam milidetik
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
    try {
        Date inDate = dateFormat.parse(checkIn);
        Date outDate = dateFormat.parse(checkOut);
        long duration = outDate.getTime() - inDate.getTime();

        // Menghitung jumlah hari tinggal
        long days = duration / (24 * 60 * 60 * 1000);

        // Menghitung total pembayaran berdasarkan lama tinggal (misal, Rp100.000 per hari)
        double totalBayar = days * 100000;

        dibayarkan = totalBayar;

        // Membuat koneksi ke database
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/akhir_oop", "root", "");

        // Menyusun query SQL untuk memasukkan data ke dalam tabel
        String query = "INSERT INTO tbl_transaksi (kode_pemesanan, pemesan, nomor_kamar, check_in, check_out, pembayaran, dibayarkan) VALUES (?, ?, ?, ?, ?, ?, ?)";

        // Membuat objek PreparedStatement untuk memasukkan data ke dalam query
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setString(1, kodePemesanan);
        stmt.setString(2, pemesan);
        stmt.setString(3, nomorKamar);
        stmt.setString(4, checkIn);
        stmt.setString(5, checkOut);
        stmt.setString(6, pembayaran);
        stmt.setDouble(7, dibayarkan);

        // Menjalankan query untuk menyimpan data ke dalam database
        stmt.executeUpdate();

        // Menutup koneksi dan statement
        stmt.close();
        conn.close();

        // Menampilkan pesan sukses
        System.out.println("Data berhasil disimpan ke database.");

        // Menampilkan total pembayaran
        System.out.println("Total Pembayaran: Rp" + totalBayar);

    } catch (SQLException e) {
        // Menampilkan pesan kesalahan jika terjadi error
        System.out.println("Error: " + e.getMessage());
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
    
    txtUang.setText(String.valueOf(dibayarkan));
    }//GEN-LAST:event_btnPesanActionPerformed

